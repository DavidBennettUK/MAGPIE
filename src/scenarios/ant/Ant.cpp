Ant::Ant(int maxMoves) : maxMoves(maxMoves)
{
    gridWidth = 32;
    gridHeight = 32;

    reset();
}

bool Ant::step()
{
    return Individual::step() &&
           points != 89 &&
           moves < maxMoves;
}

