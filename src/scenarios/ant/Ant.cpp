Individual *Ant::clone() const
{
    Ant *newAnt = new Ant(maxMoves);
    newAnt->facing = facing;
    newAnt->x = x;
    newAnt->y = y;
    newAnt->points = points;
    newAnt->moves = moves;
    newAnt->rootNode = rootNode->clone();
    newAnt->programStack = programStack;

    memcpy(&newAnt->grid, grid, sizeof(int) * gridWidth * gridHeight);
    memcpy(&newAnt->visits, visits, sizeof(int) * gridWidth * gridHeight);

    if (fitness)
        newAnt->fitness = fitness->clone();

    return newAnt;
}

Fitness *Ant::getFitness()
{
    if (fitness == NULL)
    {
        reset();

        while (step());

        fitness = new AntFitness(points, moves);
    }

    return fitness;
}

bool Ant::isFoodInFront()
{
    switch (facing)
    {
    case UP:
        return isFoodAt(x, (y - 1 + gridHeight) % gridHeight);

    case RIGHT:
        return isFoodAt((x + 1) % gridWidth, y);

    case DOWN:
        return isFoodAt(x, (y + 1) % gridHeight);

    case LEFT:
        return isFoodAt((x - 1 + gridWidth) % gridWidth, y);

    default:
        Q_ASSERT_X(false, "MowNode::evaluate", "Invalid facing direction");
        return false;
    }
}

void Ant::moveForward()
{
    switch (facing)
    {
    case UP:
        y = (y - 1 + gridHeight) % gridHeight;
        break;

    case RIGHT:
        x = (x + 1) % gridWidth;
        break;

    case DOWN:
        y = (y + 1) % gridHeight;
        break;

    case LEFT:
        x = (x - 1 + gridWidth) % gridWidth;
        break;

    default:
        Q_ASSERT_X(false, "MowNode::evaluate", "Invalid facing direction");
    }

    if (isFoodAt(x,y))
    {
        grid[y][x] = 0;
        points++;
    }

    moves++;
    visits[y][x] = 1;
}

void Ant::turn(bool left)
{
    int newDir = left ? (getFacing() - 1 + 4) % 4 : (getFacing() + 1 + 4) % 4;
    setFacing((Ant::Facing) newDir);

    moves++;
}
